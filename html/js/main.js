const mobileDevice = window.matchMedia("(max-width: 550px)");

function handleDeviceChange(e) {
    if (e.matches) {
        document.getElementById("nav-brand").innerHTML = "";
        document.getElementById("nav-ul").style.gridTemplateColumns = "8% 2fr 1fr";
    }else {
        document.getElementById("nav-brand").innerHTML = '<a href="#nav-ul">thiering</a>';
        document.getElementById("nav-ul").style.gridTemplateColumns = "1fr 1fr 1fr";
    }
}

handleDeviceChange(mobileDevice);

window.addEventListener("resize", (e) => {
    handleDeviceChange(mobileDevice)
})